class Command(BaseCommand):
    help = 'Призначення зображень до LabelingProduct і логотипів Brand'

    def add_arguments(self, parser):
        parser.add_argument('--force', action='store_true',
                            help='Перезаписати існуючі записи')

    def handle(self, *args, **options):
        force = options['force']

        from src.content.models import LabelingProduct, Brand

        self.stdout.write(self.style.MIGRATE_HEADING(
            '=== seed_media: зображення продуктів та логотипи брендів ==='))

        try:
            with transaction.atomic():
                self._seed_labeling_products(LabelingProduct, force)
                self._seed_brand_logos(Brand)
        except Exception as exc:
            raise CommandError(f'Помилка: {exc}') from exc

        self.stdout.write(self.style.SUCCESS('\n✓ seed_media завершено успішно.'))

    def _seed_labeling_products(self, model, force: bool) -> None:
        self.stdout.write('\n[1/2] Продукти етикетування...')
        for data in LABELING_PRODUCTS:
            title = data['title']
            image_path = data.pop('image')

            if force:
                obj, created = model.objects.update_or_create(
                    title=title,
                    defaults={k: v for k, v in data.items() if k != 'title'},
                )
            else:
                obj, created = model.objects.get_or_create(
                    title=title,
                    defaults=data,
                )

            # Призначаємо зображення якщо є файл
            if created or force:
                obj.image = image_path
                obj.save(update_fields=['image'])

            status = 'створено' if created else ('оновлено' if force else 'вже існує')
            self.stdout.write(
                f'  [{data["category"]}] {title[:55]} — {status}'
                f' | img: {image_path}'
            )
            data['image'] = image_path  # відновлюємо для наступних викликів

    def _seed_brand_logos(self, model) -> None:
        self.stdout.write('\n[2/2] Логотипи брендів...')
        for brand_name, logo_path in BRAND_LOGOS.items():
            try:
                brand = model.objects.get(name=brand_name)
                brand.logo = logo_path
                brand.save(update_fields=['logo'])
                self.stdout.write(f'  {brand_name} → {logo_path}')
            except model.DoesNotExist:
                self.stdout.write(self.style.WARNING(
                    f'  УВАГА: бренд «{brand_name}» не знайдений у БД'))
