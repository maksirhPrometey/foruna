class Command(BaseCommand):
    help = 'Наповнення БД реальним контентом із технічних специфікацій'

    def add_arguments(self, parser):
        parser.add_argument(
            '--force',
            action='store_true',
            help='Перезаписати існуючі записи (update_or_create)',
        )

    def handle(self, *args, **options):
        force = options['force']

        from src.content.models import (
            LaserProduct, QualityProduct, QualityCategoryContent, StatItem, HomePage,
            LabelingCategoryContent, Brand,
        )

        self.stdout.write(self.style.MIGRATE_HEADING('=== FortunaPrint — seed production data ==='))

        try:
            with transaction.atomic():
                self._seed_lasers(LaserProduct, force)
                self._seed_quality_sections(QualityCategoryContent, force)
                self._seed_quality_products(QualityProduct, force)
                self._seed_stats(StatItem, HomePage, force)
                self._seed_labeling_sections(LabelingCategoryContent, force)
                self._seed_brands(Brand, force)
        except Exception as exc:
            raise CommandError(f'Помилка при наповненні БД: {exc}') from exc

        self.stdout.write(self.style.SUCCESS('\n✓ Seed завершено успішно.'))

    # ------------------------------------------------------------------

    def _seed_lasers(self, model, force: bool) -> None:
        self.stdout.write('\n[1/4] Лазерні маркіратори...')
        for data in LASER_PRODUCTS:
            laser_type = data['laser_type']
            if force:
                obj, created = model.objects.update_or_create(
                    laser_type=laser_type,
                    defaults={k: v for k, v in data.items() if k != 'laser_type'},
                )
            else:
                obj, created = model.objects.get_or_create(
                    laser_type=laser_type,
                    defaults=data,
                )
            status = 'створено' if created else ('оновлено' if force else 'вже існує — пропущено')
            self.stdout.write(f'  {obj.get_laser_type_display()} [{laser_type}] — {status}')

    def _seed_quality_sections(self, model, force: bool) -> None:
        self.stdout.write('\n[2/4] Секції сторінки «Контроль якості»...')
        for data in QUALITY_CATEGORY_SECTIONS:
            category = data['category']
            if force:
                obj, created = model.objects.update_or_create(
                    category=category,
                    defaults={k: v for k, v in data.items() if k != 'category'},
                )
            else:
                obj, created = model.objects.get_or_create(
                    category=category,
                    defaults=data,
                )
